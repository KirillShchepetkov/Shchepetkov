# Задача №4 по циклам (Раздел: Цикл while)
#
# Напишите программу, которая:
#
# Имеет переменную counter с начальным значением 1.
# Используя цикл while, выводит на экран значение counter до тех пор, пока counter меньше или равен 5.
# После каждого вывода увеличивает counter на 1.
# Каждое значение выводится с новой строки.

#Решение:
counter = 1

while counter <= 5:
    print(counter)
    counter += 1

