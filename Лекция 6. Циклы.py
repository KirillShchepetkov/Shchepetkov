# Задача №6 по циклам (Раздел: Остановка циклов break)
#
# Напишите программу, которая:
#
# Имеет список чисел: numbers = [3, 7, 12, 5, 20, 8, 15].
# Используя цикл for, перебирает числа в списке.
# При нахождении первого числа, большего 10, цикл должен немедленно прерываться (используйте break).
# После прерывания цикла программа выводит на экран сообщение: "Найдено число больше 10: <число>".
# Примечание: Программа должна найти число 12 и остановиться, не проверяя остальные числа.

#Решение:
numbers = [3, 7, 12, 5, 20, 8, 15]

for number in numbers:
    if number > 10:
        print(f"Найдено число больше 10: {number}")
        break

