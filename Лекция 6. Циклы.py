# Задача №9 нового цикла (Раздел: Цикл while с прерыванием)
#
# Напишите программу, которая:
#
# Имеет переменную attempts с начальным значением 0.
# Используя цикл while True (бесконечный цикл), делает следующее:
# Увеличивает attempts на 1.
# Выводит на экран "Попытка № <attempts>".
# Если attempts становится равным 5, цикл прерывается (используйте break).
# После прерывания цикла выводит сообщение: "Достигнут лимит попыток".

#Решение:
attempts = 0
while True:
    attempts += 1
    print(f"Попытка № {attempts}")
    if attempts == 5:
        print("Достигнут лимит попыток")
        break