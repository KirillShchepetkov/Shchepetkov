# Задача №11 нового цикла (Раздел: Цикл while с условием и инкрементом)
#
# Напишите программу, которая:
#
# Имеет переменную power с начальным значением 1.
# Используя цикл while, выводит на экран степени двойки (2⁰, 2¹, 2², ...), пока значение не превысит 50.
# Формула: value = 2 ** power (где power — текущая степень).
# После каждого вывода увеличивает power на 1.
# Каждое значение выводится с новой строки в формате: "2^<степень> = <результат>".

#Решение:
power = 0

value = 2 ** power
while value <= 50:
    print(f"2^{power} = {value}")
    power += 1
    value = 2 ** power

# Задача №12 нового цикла (Раздел: Цикл for с прерыванием)
#
# Дан список имён пользователей:
# users = ["admin", "alice", "bob", "charlie", "diana", "admin", "eve"]
#
# Используя цикл for, перебирайте имена в списке.
# При обнаружении второго вхождения имени "admin" цикл должен немедленно прерываться (используйте break).
# Перед прерыванием выведите сообщение: "Обнаружен второй администратор".
# Подсказка: Вам понадобится счётчик для отслеживания количества найденных "admin"

#Решение:
users = ["admin", "alice", "bob", "charlie", "diana", "admin", "eve"]

admin_count = 0
for user in users:
    if user == "admin":
        admin_count += 1
        if admin_count == 2:
            print("Обнаружен второй администратор")
            break

