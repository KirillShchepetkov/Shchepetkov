# Задача №1 по циклам (Раздел: Цикл for — итерация элементов)
#
# Дан список названий браузеров:
# browsers = ["Chrome", "Firefox", "Edge", "Safari", "Opera"]
#
# Используя цикл for, пройдитесь по каждому элементу списка.
# Внутри цикла выводите каждый элемент на экран (каждый браузер с новой строки).

#Решение:
