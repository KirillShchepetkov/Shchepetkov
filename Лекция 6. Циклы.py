# Задача №15 нового раунда (Раздел: Цикл while с условием)
#
# Напишите программу, которая:
#
# Имеет переменную countdown с начальным значением 5.
# Используя цикл while, выводит на экран сообщение "Осталось секунд: <countdown>"
# до тех пор, пока countdown больше 0.
#
# После каждого вывода уменьшает countdown на 1.
# Когда цикл завершится, выводит сообщение "Старт!".

#Решение:
countdown = 5
while countdown > 0:
    print(f"Осталось секунд: {countdown}")
    countdown -= 1
print("Старт!")

