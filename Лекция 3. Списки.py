# Задача №4 по спискам (Раздел: Срезы списка)
#
# У вас есть список:
# versions = ["1.0", "1.1", "2.0", "2.1", "3.0", "3.1", "4.0"]
#
# Используя срезы (slice), создайте и сохраните в новые переменные следующие подсписки:
#
# Первые три версии.
# Последние три версии.
# Версии с третьей по пятую включительно (считая с 1).
# Каждую вторую версию, начиная с первой.
# Список в обратном порядке.
# Выведите на экран (каждый с новой строки) все полученные подсписки, а также исходный список.

#Решение:

versions = ["1.0", "1.1", "2.0", "2.1", "3.0", "3.1", "4.0"]

slice_1 = versions[:3]
print(slice_1)

slice_2 = versions[-3:]
print(slice_2)

slice_3 = versions[2:5]
print(slice_3)

slice_4 = versions[::2]
print(slice_4)

slice_5 = versions[::-1]
print(slice_5)

print(versions)

# Задача №5 по спискам (Раздел: Слияние списков)
#
# У вас есть два списка:
# team_a = ["Анна", "Борис", "Виктор"]
# team_b = ["Галина", "Дмитрий", "Елена"]
#
# Создайте новый список all_team, который является слиянием двух списков (team_a и team_b) с помощью оператора +.
# С помощью метода добавьте в конец списка all_team нового сотрудника "Жанна".
# С помощью метода расширения добавьте к списку all_team список
# interns = ["Игорь", "Ксения"] (т.е. добавьте эти два имени как отдельные элементы в конец).
#
# Выведите на экран:
# Итоговый список all_team.
# Длину итогового списка (количество элементов).

#Решение:

team_a = ["Анна", "Борис", "Виктор"]
team_b = ["Галина", "Дмитрий", "Елена"]

all_team = team_a + team_b

all_team.append("Жанна")

interns = ["Игорь", "Ксения"]

all_team.extend(interns)

length = len(all_team)

print(all_team)
print(length)

