# Задача №7 по спискам (Раздел: Замена элементов в списке)
#
# У вас есть список уровней логирования:
# log_levels = ["DEBUG", "INFO", "WARNING", "ERROR", "CRITICAL", "INFO", "DEBUG"]
#
# Замените первый элемент списка на "TRACE".
#
#Замените последний элемент списка на "FATAL".
#
# Найдите индекс второго вхождения элемента "INFO" в списке и замените этот элемент на "NOTICE".
# Используйте метод для поиска индекса с параметром start, чтобы найти именно второе вхождение.
#
# Выведите на экран итоговый список.

#Решение:

log_levels = ["DEBUG", "INFO", "WARNING", "ERROR", "CRITICAL", "INFO", "DEBUG"]

log_levels[0] = "TRACE"

log_levels[-1] = "FATAL"

first_index = log_levels.index("INFO")
second_index = log_levels.index("INFO", first_index + 1)

log_levels[second_index] = "NOTICE"

print(log_levels)

