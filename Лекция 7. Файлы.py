# Задача №5 по файлам (Раздел: Работа с файлами — чтение и запись в одном контексте)
#
# Создайте (или откройте, если существует) файл counter.txt.
#
# Прочитайте текущее содержимое файла.
# Предполагается, что в файле записано одно целое число (например, 0).
# Если файл пуст или не существует, считайте начальное значение 0.
#
# Увеличьте прочитанное число на 1.
# Запишите обновлённое число обратно в файл (перезаписав старое значение).
# Выведите на экран сообщение: "Текущее значение счётчика: <число>".
#
# Подсказка: Используйте режим "r+" для чтения и записи.
# Не забудьте преобразовать строку в число и обратно. Обработайте случай, когда файл пуст.

#Решение:

with open("counter.txt", "r+") as file:
    content = file.read()
    if content.strip():
        numb = int(content)
    else:
        numb = 0

    numb += 1

    file.seek(0)
    file.write(str(numb))
    file.truncate()

print(f"Текущее значение счётчика: {numb}")

