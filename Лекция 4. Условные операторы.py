    if status_client == "regular":
        discount_percent_2 = 0
        discount_rub_2 = sum_purchase * discount_percent_2 / 100
        final_sum_2 = sum_purchase - discount_rub_2
        print(discount_rub_2)
        print(final_sum_2)
    elif status_client == "vip":
        discount_percent_3 = 5
