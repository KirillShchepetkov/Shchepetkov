# Задача №6 по условиям (Раздел: Условный оператор if и логические операции)
#
# Напишите программу для проверки доступа:
#
# Программа запрашивает у пользователя возраст (целое число)
# и наличие разрешения от родителей (yes/no в любом регистре).
#
# Условия доступа:
# Если возраст 18 лет или больше — доступ разрешён.
# Иначе если возраст от 14 до 17 лет включительно И есть разрешение от родителей (yes) — доступ разрешён.
# В остальных случаях — доступ запрещён.
# Программа должна выводить только одно сообщение:
# "Доступ разрешён" — если выполнено одно из условий выше.
# "Доступ запрещён" — во всех остальных случаях.
#
# Используйте if-elif-else и логические операции

#Решение:

age = int(input("Введите целое число : " ))
permission = input("Есть разрешение от родителей? (yes/no): ")

if age >= 18:
    print("Доступ разрешён")
elif 14 <= age <= 17  and permission.lower() == "yes":
    print("Доступ разрешён")
else:
    print("Доступ запрещён")

# Задача №7 по условиям (Раздел: Вложенные условия)
#
# Напишите программу для калькулятора скидок:
# Программа запрашивает у пользователя сумму покупки (целое число)
# и статус клиента (regular — обычный, vip — постоянный).
#
# Правила расчёта скидки:
# Если сумма покупки больше 5000:
# Для regular → скидка 5%
# Для vip → скидка 10%
# Иначе (сумма покупки 5000 или меньше):
# Для regular → скидка 0%
# Для vip → скидка 5%
# Программа должна вычислить размер скидки и итоговую сумму к оплате.
# Выведите на экран (каждое с новой строки):
# Размер скидки в рублях.
# Итоговую сумму к оплате.
#
# Формулы:
# скидка_руб = сумма_покупки * (процент_скидки / 100)
# итоговая_сумма = сумма_покупки - скидка_руб
#
# Используйте вложенные условия (if внутри if).

#Решение:

sum_purchase = int(input("Сумма покупки: "))
status_client = input("Cтатус клиента (regular/vip): ")

if sum_purchase > 5000:
    if status_client == "regular":
        discount_percent = 5
        discount_rub = sum_purchase * discount_percent / 100
        final_sum = sum_purchase - discount_rub
        print(discount_rub)
        print(final_sum)
    elif status_client == "vip":
        discount_percent_1 = 10
        discount_rub_1 = sum_purchase * discount_percent_1 / 100
        final_sum_1 = sum_purchase - discount_rub_1
        print(discount_rub_1)
        print(final_sum_1)
else:
    if status_client == "regular":
        discount_percent_2 = 0
        discount_rub_2 = sum_purchase * discount_percent_2 / 100
        final_sum_2 = sum_purchase - discount_rub_2
        print(discount_rub_2)
        print(final_sum_2)
    elif status_client == "vip":
        discount_percent_3 = 5
        discount_rub_3 = sum_purchase * discount_percent_3 / 100
        final_sum_3 = sum_purchase - discount_rub_3
        print(discount_rub_3)
        print(final_sum_3)

# Задача №8 по условиям (Раздел: Логические операции и if-elif-else)
#
# Напишите программу для проверки сложного условия:
# Запрашивает у пользователя:
# temperature — целое число (температура процессора в °C)
# fan_speed — целое число (скорость вентилятора, % от 0 до 100)
# is_critical — строку "yes" или "no" (критическая задача, да/нет)
#
# Условия для нормальной работы:
#
# Норма: temperature < 80 И fan_speed >= 50
# Допустимо при критической задаче: temperature < 90 И is_critical == "yes"
# Во всех остальных случаях — перегрев.
# Выводит одно из сообщений:
#
# "Норма" — если выполняется первое условие.
# "Допустимо" — если первое условие не выполняется, но выполняется второе.
# "Перегрев!" — если не выполняется ни одно из условий.
#
# Используйте if-elif-else с логическими операциями.

# Решение:

temperature = int(input("температура процессора в °C"))
fan_speed =  int(input("скорость вентилятора % (0-100)"))
is_critical = input("Критическая задача, (yes/no): ").lower()

if temperature < 80 and fan_speed >= 50:
    print("Норма")
elif temperature < 90 and is_critical == "yes":
    print("Допустимо")
else:
    print("Перегрев!")