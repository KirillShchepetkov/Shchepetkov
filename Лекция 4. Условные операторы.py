# Задача №1 по условиям (Раздел: Оператор if)
#
# Напишите программу, которая:
#
# Запрашивает у пользователя целое число (используйте input() и преобразуйте в int).
# Проверяет с помощью оператора if, больше ли это число 100.
# Если условие выполняется, выводит на экран строку: "Число больше 100".
# Программа должна содержать только оператор if (без else/elif).

#Решение:

number = int(input("Введите целое число: "))
if number > 100:
    print("Число больше 100")

# Задача №2 по условиям (Раздел: Оператор if-else)
# Напишите программу, которая:
#
# Запрашивает у пользователя целое число (используйте input() и преобразуйте в int).
# С помощью операторов if-else проверяет:
# Если число чётное (остаток от деления на 2 равен 0), выводит строку: "Число чётное".
# Иначе (во всех остальных случаях) выводит строку: "Число нечётное".
# Программа должна содержать только операторы if и else (без elif).

#Решение:

number = int(input("Введите целое число: "))
if number % 2 == 0:
    print("Число чётное")

else :
    print("Число нечётное")

# Задача №3 по условиям (Раздел: Оператор elif)
#
# Напишите программу, которая:
# Запрашивает у пользователя оценку по пятибалльной системе (целое число от 1 до 5).
# С помощью операторов if-elif-else определяет текстовое описание оценки:
#
# Если 5 → выводит "Отлично"
# Если 4 → выводит "Хорошо"
# Если 3 → выводит "Удовлетворительно"
# Если 2 → выводит "Неудовлетворительно"
# Если 1 → выводит "Плохо"
#
# Во всех остальных случаях (число не от 1 до 5) → выводит "Некорректная оценка"
# Программа должна содержать полную цепочку if-elif-else.

#Решение:

x = int(input("Введите оценку (1-5): "))
if x == 5:
    print("Отлично")
elif x == 4:
    print("Хорошо")
elif x == 3:
    print("Удовлетворительно")
elif x == 2:
    print("Неудовлетворительно")
elif x == 1:
    print("Плохо")
else:
    print("Некорректная оценка")

