# Задача №3 по условиям (Раздел: Оператор elif)
#
# Напишите программу, которая:
# Запрашивает у пользователя оценку по пятибалльной системе (целое число от 1 до 5).
# С помощью операторов if-elif-else определяет текстовое описание оценки:
#
# Если 5 → выводит "Отлично"
# Если 4 → выводит "Хорошо"
# Если 3 → выводит "Удовлетворительно"
# Если 2 → выводит "Неудовлетворительно"
# Если 1 → выводит "Плохо"
#
# Во всех остальных случаях (число не от 1 до 5) → выводит "Некорректная оценка"
# Программа должна содержать полную цепочку if-elif-else.

#Решение:

x = int(input("Введите оценку (1-5): "))
if x == 5:
    print("Отлично")
elif x == 4:
    print("Хорошо")
elif x == 3:
    print("Удовлетворительно")
elif x == 2:
    print("Неудовлетворительно")
elif x == 1:
    print("Плохо")
else:
    print("Некорректная оценка")

