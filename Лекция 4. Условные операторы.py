# Напишите программу, которая:
# Запрашивает у пользователя два целых числа: a и b.
#
# Используя логические операции (and, or, not) внутри условий,
# проверяет и выводит результаты (True или False) для следующих случаев (каждый с новой строки):
#
# Оба числа положительные (больше 0).
